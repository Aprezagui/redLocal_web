package com.vistas;

public class IpConexion {
	private int idTipoConexion;
	private boolean dchp;
	private String ip;
	private String mascara;
	public int getIdTipoConexion() {
		return idTipoConexion;
	}
	public void setIdTipoConexion(int idTipoConexion) {
		this.idTipoConexion = idTipoConexion;
	}
	public boolean isDchp() {
		return dchp;
	}
	public void setDchp(boolean dchp) {
		this.dchp = dchp;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getMascara() {
		return mascara;
	}
	public void setMascara(String mascara) {
		this.mascara = mascara;
	}
	
	

}
